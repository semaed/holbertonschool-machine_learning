#!/usr/bin/env python3
"""
    Module content:
        - np_matmul: Matrix multiplication
"""
import numpy as np


def np_matmul(mat1, mat2):
    """
        np_matmul - Matrix multiplication

        @mat1: First matrix
        @mat2: Second matrix

        Return: New matrix with result of multiplication
    """
    return np.matmul(mat1, mat2)
